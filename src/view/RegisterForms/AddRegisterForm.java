    private void showClassroom() {
        classroomDao = new ClassroomDao();
        List<Classroom> classrooms = classroomDao.getAllClassrooms();

        for (Classroom classroom : classrooms) {
            cbxClassroom.addItem(classroom.getMaLop());
        }
    }

    private void showSubject() {
        List<Subject> subjects = MhDao.getAllSubjects();

        for (Subject subject : subjects) {
            cbxMaMH.addItem(subject.getMamh());
        }
    }

    private void showTeacher() {
        List<Teacher> teachers = GvDao.getAllTeachers();

        for (Teacher teacher : teachers) {
            cbxMaGV.addItem(teacher.getMagv());
        }
    }

