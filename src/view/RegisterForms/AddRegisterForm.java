    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {

            boolean check = true;
            //reset arlet 
            setArlet(false);

            //set arlet 
            //1
            if (tf_SoCauThi.getText().trim().length() == 0) {
                createAlert(arletSoCauThi, "Không bỏ trống Số câu thi");
                check = false;
            } else if (tf_SoCauThi.getText().trim().matches("\\d+") == false) {
                createAlert(arletSoCauThi, "Chỉ nhập số nguyên");
                check = false;
            }
            //2
            if (tf_ThoiGian.getText().trim().length() == 0) {
                createAlert(arletThoiGian, "Không bỏ trống Thời gian");
                check = false;
            } else if (tf_ThoiGian.getText().trim().matches("\\d+") == false) {
                createAlert(arletThoiGian, "Chỉ nhập số nguyên");
                check = false;
            }
            //get the input
            if (check == true) {
                getInput();
            }
            //recheck value of int
            //1
            if (dky.getSoCauThi() < 10 || dky.getSoCauThi() > 100) {
                if (check) {
                    createAlert(arletSoCauThi, "Chỉ nhận giá trị trong khoảng [10,100]");
                    check = false;
                }
            }
            //2
            if (dky.getThoiGian() < 10 || dky.getThoiGian() > 100) {
                if (check) {
                    createAlert(arletThoiGian, "Chỉ nhận giá trị trong khoảng [15,60]");
                    check = false;
                }
            }
            if (check == true)
            {
                if(dky_dao.checkRegister(dky.getMamh(), dky.getMalop(), dky.getLan())==1)
                {
                    JOptionPane.showMessageDialog(rootPane, "Đăng ký này đã tồn tại");
                    check = false;
                }
            }
            //after the check
            if (check == true) {
                if (dky_dao.addRegister(dky)) {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
                }
            }

        } catch (Exception e) {
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

